          <p>ALMI inició su actividad en 1939 con enseñanzas no regladas para la preparación de administrativos y secretarias, con sede en la Gran Vía de Bilbao. A finales de los 50 se trasladó a la Plaza del Ensanche. En 1970 con la creación de la Formación Profesional, ALMI llegó a tener 33 aulas concertadas y 1.300 alumnos. La reforma educativa de la formación profesional y la desaparición del Primer Grado supuso una gran reducción en el número de aulas y de alumnos. En el 2003 ALMI se trasladó a su ubicación actual, continuando su dedicación a la FP, además de la formación de PCPI administrativa. En la actualidad el número de aulas concertadas es de 14 y hay aproximadamente 300 alumnos de Ciclos Formativos a los que hay que añadir los alumnos de cursos de formación a trabajadores en activo como a desempleados. En julio de 2008 la sociedad se transformó en SAL, siendo en la actualidad los titulares de la misma sus propios trabajadores.</p>
